void main() {
  var variabel1 = 20;
  var variabel2 = 8;
  //Pertambahan
  variabel1 + variabel2;
  print('hasil dari 20 + 8 = ${variabel1 + variabel2}');
  //Pengurangan
  variabel1 - variabel2;
  print('hasil dari 20 - 8 = ${variabel1 - variabel2}');
  //Perkalian
  variabel1 * variabel2;
  print('hasil dari 20 * 8 = ${variabel1 * variabel2}');
  //Pembagian,hasil double
  variabel1 / variabel2;
  print('hasil dari 20 / 8 = ${variabel1 / variabel2}');
  //Pembagian, hasil int
  variabel1 ~/ variabel2;
  print('hasil dari 20 ~/ 8 = ${variabel1 ~/ variabel2}');
  //Sisa Bagi
  variabel1 % variabel2;
  print('hasil dari 20 % 8 = ${variabel1 % variabel2}');
}